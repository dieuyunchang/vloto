def has_3_odd_3_even?(numbers)
  # Check if the group has exactly 3 even and 3 odd numbers
  even_count = numbers.count { |n| n.even? }
  even_count == 3
end

# Function to generate a valid group
def generate_group(existing_groups)
  loop do
    # Generate 6 unique numbers between 1 and 45
    numbers = (1..45).to_a.sample(6).sort
    # Format numbers to two digits (e.g., 1 -> "01")
    group = numbers.map { |n| format("%02d", n) }.join(" ")

    # Ensure the group is unique and meets the criteria
    if !has_3_odd_3_even?(numbers) && !group_exists?(group, existing_groups)
      return group
    end
  end
end

